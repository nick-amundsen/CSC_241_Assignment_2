
public class Vertex {
	String name;
	double xcoord;
	double ycoord;
	public Vertex(String name) {
		this.name = name;
	}
	public double getXcoord() {
		return xcoord;
	}
	public void setXcoord(double d) {
		this.xcoord = d;
	}
	public double getYcoord() {
		return ycoord;
	}
	public void setYcoord(double ycoord) {
		this.ycoord = ycoord;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
